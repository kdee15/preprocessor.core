// A. END +++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++

// B. CONTROLLER ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++

globalController.controller(
    'ctrlGlobal', [ 
        '$scope',
        '$state',
        function($scope, $state) {
            
            // B.1. HELPER VARIABLES ----------------------------------------------------------------------------------
            
            $scope.snippets = 'assets/includes/snippets/';
            $scope.sections = 'assets/includes/sections/';
            
            // B.1. END -----------------------------------------------------------------------------------------------
            
            // B.2. SAY HELLO -----------------------------------------------------------------------------------------
            
            console.log('Hello Angular!');
            
            // B.2. END -----------------------------------------------------------------------------------------------

        }
    ]
);

